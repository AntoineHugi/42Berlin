#include "libft.h"

t_list	*ft_lstnew(int value)
{
	t_list	*new_elem;

	new_elem = (t_list *)malloc(sizeof(t_list));
	if (!new_elem)
		return (NULL);
	new_elem->value = value;
	new_elem->index = 0;
	new_elem->pos = 0;
	new_elem->next = NULL;
	return (new_elem);
}
